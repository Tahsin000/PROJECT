    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String inputBus_No = jTextField2.getText();
        String inputDriving_name= jTextField6.getText();
        String inputDriving_phone_no = jTextField4.getText();
        String inputLicence_number = jTextField5.getText();
        String inputOwner = jTextField1.getText();
        String inputstatus= "Available"; 
        
        if (jRadioButton2.isSelected())
        {
            if (inputBus_No.equals("") || inputDriving_name.equals("") || inputDriving_phone_no.equals("") ||  
                    inputLicence_number.equals("") ||  inputOwner.equals(""))
            {
                JOptionPane.showMessageDialog(this, "Fill out the complete form !");
            }
            else
            {
                int k = 0;
                try 
                {
                    pst=con.prepareStatement("insert into bus_add(bus_no,driving_name,driving_phone_no,licence_number,owner,bus_status)"
                            + "values(?,?,?,?,?,?)");
                    pst.setString(1, inputBus_No);
                    pst.setString(2, inputDriving_name);
                    pst.setString(3, inputDriving_phone_no);
                    pst.setString(4, inputLicence_number);
                    pst.setString(5, inputOwner);
                    pst.setString(6, inputstatus);
                    
                    k = pst.executeUpdate();
                    if(k==1)
                    {
                        JOptionPane.showMessageDialog(this, "SAVE ! ");
                    }
                    else 
                    {
                        JOptionPane.showMessageDialog(this,"Error");
                    }
                } 
                catch (SQLException ex) 
                {
                    
                    try {
                        pst1=con.prepareStatement("select * from bus_add where bus_no = '"+inputBus_No+"'");
                        rs = pst1.executeQuery();
                        if (rs.next())
                        {
                            JOptionPane.showMessageDialog(this,"Bus number is alrady add ! ðŸ˜Ž");
                        }
                        else 
                        {
                            JOptionPane.showMessageDialog(this,"Licence number is alrady add ! ðŸ˜Ž");
                        }
                    } catch (SQLException ex1) {
                        Logger.getLogger(AdminHome_AddBus.class.getName()).log(Level.SEVERE, null, ex1);
                    }
                    Logger.getLogger(Manu.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (k == 1)
                {
                    try {
                        pst3 = con.prepareStatement("insert into add_owner VALUES ('"+inputOwner+"')");
                        int p = pst3.executeUpdate();
                    } catch (SQLException ex) {
                        Logger.getLogger(AdminHome_AddBus.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
        else
        {
            if (jTextField3.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Fill out the complete location !");
            }
            else 
            {
                try {
                pst1=con.prepareStatement("insert into add_location VALUES('"+jTextField3.getText()+"')");
                int k = pst1.executeUpdate();
                if(k==1)
                {
                    JOptionPane.showMessageDialog(this, "SAVE ! ");
                }
                else 
                {
                    JOptionPane.showMessageDialog(this,"Error");
                }
                }catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this,"Same location not allow !");
                    Logger.getLogger(AdminHome_AddBus.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
//        else 
//        {
//            JOptionPane.showMessageDialog(this, "Password not match !");
//        }
    }//GEN-LAST:event_jButton1ActionPerformed

