    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(jRadioButton2.isSelected())
        {
            try
            {
                pst=con.prepareStatement("DELETE FROM sign_up WHERE id = ?");
                pst.setString(1, check_id);
                int k = pst.executeUpdate();
                if (k == 1)
                {
                    JOptionPane.showMessageDialog(this, "DELETE !");
                    tableupdate1();
                }
            }
            catch (SQLException ex)
            {
                Logger.getLogger(User_info.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if (jRadioButton1.isSelected())
        {
             try
            {
                pst=con.prepareStatement("DELETE FROM guest_info WHERE name = ? and ans = ?");
                pst.setString(1, check_name);
                pst.setString(2, check_ans);
                int k = pst.executeUpdate();
                if (k == 1)
                {
                    JOptionPane.showMessageDialog(this, "DELETE !");
                    tableupdate2();
                }
            }
            catch (SQLException ex)
            {
                Logger.getLogger(User_info.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

