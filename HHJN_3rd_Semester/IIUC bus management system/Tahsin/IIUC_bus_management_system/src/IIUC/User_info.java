    public void tableupdate1() { 
        try 
        {
            pst = con.prepareStatement("SELECT * FROM sign_up");
            rs=pst.executeQuery();
            DefaultTableModel d= (DefaultTableModel)jtable1.getModel();
            d.setRowCount(0);
            while(rs.next())
            {
                String var1= rs.getString("username");
                String var2= rs.getString("gender");
                String var3 = rs.getString("id");
                String var4 = rs.getString("password");
                String var5 = rs.getString("location");
                d.addRow(new Object[]{var1, var2, var3, var4, var5});
            }
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(Regular_schedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void tableupdate2() { 
        try 
        {
            pst1 = con.prepareStatement("SELECT * FROM guest_info");
            rs2=pst1.executeQuery();
            DefaultTableModel d= (DefaultTableModel)jtable2.getModel();
            d.setRowCount(0);
            while(rs2.next())
            {
                String var1= rs2.getString("name");
                String var2= rs2.getString("gender");
                String var3 = rs2.getString("ans");
                d.addRow(new Object[]{var1, var2, var3});
            }
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(Regular_schedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
