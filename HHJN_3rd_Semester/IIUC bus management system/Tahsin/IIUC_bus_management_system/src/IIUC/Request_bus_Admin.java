    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       String inputStatus = jComboBox2.getSelectedItem().toString();
       String inputBusNo = jComboBox1.getSelectedItem().toString(), inputFrom = jComboBox3.getSelectedItem().toString();
       String inputTime = jTextField6.getText();
       Date t = new Date();
       SimpleDateFormat dF= new SimpleDateFormat("dd/MM/yyyy");
        
       if(inputStatus.equals("Approved"))
       {
           try 
                {   
                pst=con.prepareStatement("update request_bus set `target` = '"+inputStatus+"',`busno` = '"+inputBusNo+"'"
                        + ", `from` = '"+inputFrom+"', `time` = '"+inputTime+"', `date` = '"+dF.format(t).toString()+"' "
                                + "where id ='"+check_ID+"'");
                        
                int k = pst.executeUpdate();
                if(k==1)
                {
                    JOptionPane.showMessageDialog(this, "Save ! ");
                    tableupdate();
                }
                else 
                {
                   JOptionPane.showMessageDialog(this,"Error");
                }    
            } 
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(this,"Error");
                Logger.getLogger(Add_Schedule.class.getName()).log(Level.SEVERE, null, ex);
            }
       }
       else 
       {
           try 
                {   
                pst1=con.prepareStatement("update request_bus set date = '"+dF.format(t).toString()+"', target = '"+inputStatus+"' where id ='"+check_ID+"'");
                        
                int k = pst1.executeUpdate();
                if(k==1)
                {
                    JOptionPane.showMessageDialog(this, "Save ! ");
                    tableupdate();
                }
                else 
                {
                   JOptionPane.showMessageDialog(this,"Error");
                }    
            } 
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(this,"Error");
                Logger.getLogger(Add_Schedule.class.getName()).log(Level.SEVERE, null, ex);
            }
       }
    }//GEN-LAST:event_jButton1ActionPerformed

