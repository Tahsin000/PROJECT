    public void SearchTable()
    {
        DefaultTableModel table = (DefaultTableModel)jtable1.getModel();
        String search = jTextField1.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(table);
        jtable1.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }
    
    public void tableupdate() { 
        try 
        {
            String s = jTextField1.getText();
            pst = con.prepareStatement("SELECT * FROM request_bus where target = 'pending'");
            rs=pst.executeQuery();
            DefaultTableModel d= (DefaultTableModel)jtable1.getModel();
            d.setRowCount(0);
            while(rs.next())
            {
                String var1= rs.getString("id");
                String var2= rs.getString("destination");
                String var3= rs.getString("target");
                String var4= rs.getString("busno");
                String var5= rs.getString("from");
                String var6= rs.getString("time");
                d.addRow(new Object[]{var1, var2, var3, var4, var5, var6});
            }
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(Regular_schedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
